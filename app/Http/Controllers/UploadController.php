     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(ImageUploadRequest $request)
    {
        $src = $request->image->store('gallery', 'public');

        $image = new Image;
        $image->src = $src;
        $image->save();

        return back();
    }

